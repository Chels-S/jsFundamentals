//Comparison Operators

//Equal to
'3' == 3;
console.log('3'==3);

//Strict Equal to
//Compares the datatype and the value of the two values to check if thye are equal

console.log(3 === 3);
console.log('3' === 3);

//Not Equal to
console.log('3' != 3); //Basically. saying '3' is not equal to 3, but this is false because it's equal to not strict equal

//Strict Not Equal to
console.log('3' !== 3); //This one comes back true because it is strict not equals

//Greater than
console.log(3 > 2);

//Less than
console.log(3 < 2);

//Greater than or Equal to
console.log(3 >= 2);

//Less than or Equal to
console.log(3 <= 2);

//'And' Operator 
2 && 3;

//'Or' Operator
2 || 3;